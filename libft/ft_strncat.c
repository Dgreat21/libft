#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (s1[i])
		i++;
	j = 0;
	while (s2[j] && n > 0)
	{
		s1[i] = s2[j];
		i++;
		n--;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
